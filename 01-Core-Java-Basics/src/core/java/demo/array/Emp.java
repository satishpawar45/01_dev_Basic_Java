package core.java.demo.array;

public class Emp {

	int eid;
	String ename;

	public Emp(int i, String name) {
		eid = i;
		ename = name;
	}
	public int getEid() {
		return eid;
	}

	public void setEid(int eid) {
		this.eid = eid;
	}

	public String getEname() {
		return ename;
	}

	public void setEname(String ename) {
		this.ename = ename;
	}

}
