public class Variables {
	public static void main(String[] args) {

		int intVar = 1000000000;
		short smallNum = 3413;
		byte reallySmall = -128;
		double decimalNum = 434.434;
		boolean decision = false;
		char letter = '$';

		// Java is strongly typed language

		// double i="dwfd";

		System.out.println("Demo of the varibles");
		System.out.println("Int var is" + intVar);
		System.out.println("smallNum is" + smallNum);
		System.out.println("reallySmall is" + reallySmall);
		System.out.println("decimalNum is" + decimalNum);
		System.out.println("decision is" + decision);
		System.out.println("letter is" + letter);
	}
}
